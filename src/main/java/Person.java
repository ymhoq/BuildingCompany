import java.util.Objects;

public abstract class Person implements IMessege{
    private String firstName;
    private String lastName;
    private int age;
    private int phoneNumber;
    private String email;

    public Person() {
    }
    public Person(String firstName, String lastName, int age, int phoneNumber, String email) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.age = age;
        this.phoneNumber = phoneNumber;
        this.email = email;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(int phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    public String toString() {
        return "first name : " + firstName
                + "last name : " + lastName + "\n";
    }

    public boolean equals(Object o) {
        if (this == o)
            return true;
        if (o == null || getClass() != o.getClass())
            return false;
        Person that = (Person) o;
        return Objects.equals(this.lastName, that.lastName) && Objects.equals(this.getFirstName(), that.getFirstName());
    }
}
